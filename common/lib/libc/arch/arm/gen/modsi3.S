#include <machine/asm.h>

/* 
 * stack is aligned as there's a possibility of branching to .L_overflow
 * which makes a C call
 */

ENTRY(__modsi3)
	stmfd	sp!, {lr}
	sub	sp, sp, #4	/* align stack */
	bl	PIC_SYM(__divsi3, PLT)
	add	sp, sp, #4	/* unalign stack */
	mov	r0, r1
	ldmfd	sp!, {pc}

